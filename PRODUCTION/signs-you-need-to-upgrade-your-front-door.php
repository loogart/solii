                        <p>Although it’s the focal point of every entrance and exit we make in our home, front door maintenance is often overlooked.</p>

                        <p>As fanatics of beautiful doors and windows, we want to shine a spotlight on your trusty front door, because after all, it’s key (pun totally intended!) to creating the perfect first impression. </p>
                        <p>Not only is your front door a great way to invest in guaranteed curb appeal during resale, but it also sets the tone for what’s to come. How do you know it’s time for an upgrade, though? Here are 5 signs that you may want to invest in a new front door, and when that happens - <a href="https://solii.ca/contact">call us</a>! ;)</p>

                        <h3>1. You’re Feeling a Draft</h3>
                        <p>The most obvious sign your front door needs replacing; you can feel cold air coming into your home from around it. No need to suffer through Ottawa’s already frigid winters—start thinking about a new door as soon as you feel that unwelcome draft. </p>

                        <h3>2. It Looks Outdated </h3>

                        <p>Perhaps it’s a bit of rust here, a stubborn scratch that paint won’t fix, or maybe it’s just looking tired. If you’re feeling like your door doesn’t represent your home best, it’s probably time to upgrade your front door to something fresh and welcoming.</p>

                        <h3>3. Tough to Open and Close </h3>
                        <p>Over time, doors can warp and swell, especially in extreme climates like Ottawa. This can result in a struggle when opening and closing. If your door isn’t up to the simple task of opening and closing, it’s time to replace it. Patio and sliding doors should also be checked to make sure they’re operating smoothly. At Solii, we use thick 3-piece staggered grain wood for our wood doors. This creates a higher resistance to warping and helps our doors have a much, much longer life.</p>

                        <h3>4. Scrapes the Floor</h3>
                        <p>Doors also tend to sag over time. If yours scrapes the floor as you operate it, this is a sure sign that you’re in need of a new one. Don’t wait until your floors are ruined beyond repair. </p>

                        <h3>5. Moisture Between Glass Panes</h3>
                        <p>If your door has glass panes and you see moisture in between the panes, this means that the seal has been compromised and the door’s insulation properties are not top notch. The moisture could also develop into mold. No bueno - time to think about getting a new door. </p>
