                        <h4>Window Features</h4>
                        <p>Don’t overlook the small stuff; muntins, screens, hardware, and finishes will all impact the overall look of your home, so get to know your aesthetic prior to meeting with your designer, architect, builder, or window manufacturer. This will help you choose the right finishing touches.</p>

                        <h3>3. Economic &amp; Energy Efficiency</h3>

                        <!-- IMAGE -->
                        <img src="./img/gallery/fixed_1.jpg" width="640">

                        <h4>Glass</h4>
                        <p>Especially in extreme climates like Ottawa, choosing a glass that offers optimal efficiency is important. Choose from double or triple-glazed, in addition to low-e and argon-filled glass options for best performance.</p>
                        <h4>Installation</h4>
                        <p>Professional installation helps minimize the risks (depending on the environment) of infiltration or potential water damage, and is often necessary for warranties.</p>
                        <h4>Placement</h4>
                        <p>If building your home from scratch, proper window placement based on your home’s orientation can impact how the elements move through your home. Alternatively, you can choose specific glass coatings for different areas of your home, depending on sun exposure.</p>
                        <h4>Frame Construction</h4>
                        <p>Different frame materials offer different insulating factors which will affect the efficiency of your windows. Wood is naturally insulated, while aluminum must be thermally broken (i.e. manufactured with a barrier in between the inside and outside window frames that will prevent the conductive thermal energy loss).</p>

                        <h3>4. Good Things To Know <i>Before</i> Contacting Your Architect, Designer, Builder, Or Window Specialist</h3>
                        <ul>
                            <li>Know what type of home you have (i.e. farmhouse, ranch style, bungalow, two-storey, etc.)</li>
                            <li>Know the (approximate) age of your home</li>
                            <li>Know how many windows you need, or want</li>
                            <li>Know what material you prefer (i.e. wood, aluminum, etc.)</li>
                            <li>Know which style you would like to incorporate</li>
                            <li>Know your (approximate) budget </li>
                        </ul>

                        <h3>5. Good Questions To Ask</h3>
                        <ul>
                            <li>What is the turnaround time between ordering and installation?</li>
                            <li>How long will installation take?</li>
                            <li>What happens if there is an issue after product installation? Ask for a breakdown of the warranty.</li>
                        </ul>

