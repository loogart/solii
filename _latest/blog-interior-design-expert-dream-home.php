                        <h4>Inside Story: Axiom Interiors and an Insider’s View on Creating Your Dream Home</h4>

                        <p>We’re proud that every Gemstone project has great curb appeal, but just like the old relationship adage, what’s inside counts too. Stellar interior home design is part of what draws our clients in and keeps them there, which is why we’re introducing you to our right hand in interior design. Meet Thu Vu at Axiom Interiors.</p>

                        <p>Three years ago, Thu came on board to complete 894 Colonel By Drive (hyperlink to this project page), and since then her role as our trusted partner in interior design has yielded the innovative and beautiful spaces we are known for. Our common values of innovation, producing a high quality product and attention to detail result in construction and development projects we’re all proud of. </p>

                        <p>We caught up with Thu to get her thoughts on the building and development climate in Ottawa, interior design trends in 2018, and her thoughts on creating an interior space you’ll want to live in forever. </p>

                        <h3>1 - There really is so much out there - how do you stay on top of what’s current?</h3>
                        <p>I make sure to keep up with design blogs, I read up on new products and attend design shows. Even if I see something that’s totally wild, I like to keep it in the back of my mind to inspire my future projects. We are lucky the internet is full of inspiration and I can keep up with trends at an international level. </p>

                        <h3>2 - What are your go-to tips to make a home feel welcoming and unique?</h3>
                        <p>My recommendation is to start with a neutral palette - a base of something that you can live with for a long time, then accessorize with furniture, cushions, and art that pops and personalizes your space. However you choose to accessorize, it should always reflect and define YOU.</p>

                        <h3>3 - We have to ask, what have been your favourite Gemstone projects to work on to date, and why do you think they were successful partnerships?</h3>

                        <p>Colonel By (link to project page) and 252 Argyle (link to project page or previous blog post) were great projects to work on. 252 Argyle was especially fun because it was a Catholic residence so there were bizarre outdated features like closed-in layouts, and tiny rooms with mismatched finishes and bathroom sinks. </p>

                        <strong>How did you see past all that? </strong>

                        <p>I love doing renos like that because it allows you to envision what it could be and clients really appreciate the process of developing a new, functional and fresh space from something that seems completely impractical. It opens up their mind about design, so it’s important to be able to see beyond what a space could be. </p>

                        <p>I think the project was successful because we were both willing to compromise to get the best result. In the end, 90% of my original ideas were implemented, which is the product of a great shared vision. </p>

                        <p>A big reason why I enjoy working with Gemstone is because of the value for interior design, which allows me to be creative and explore unconventional ideas. There is also respect each others’ expertise, which is always the foundation of a great partnership.
                        </p>

                        <h3>4 - As you know, we like to stand out from the crowd. What does Axiom do that is different than its competitors?</h3>

                        <p>We get very involved in the design - a lot like Gemstone’s hands-on approach. We don’t hand projects off, we want to make sure that every project represents Axiom and that the concepts are followed-through. Aside from structural compromises, we listen to our clients needs so that changes are few and far between. Having control over the process and construction is important to us. </p>

                        <p>Once the design is completed we stay with the team, keeping in close contact until the project is complete, and we work on behalf of the client with the construction team.
                        </p>

                        <h3>5 - What are your thoughts on the construction and development market in Ottawa? </h3>

                        <p>Lucky for us we’re in a city that’s sheltered from a highly fluctuating economy. A positive change I see is that builders are slowly growing to value interior design, I see a lot of builders making an effort in differentiating their homes. This ultimately provides unique options for Ottawa dwellers, which is a great positive change. </p>

                        <h3>6 - What are the design trends for 2018 and how can developers and builders keep up with them?</h3>

                        <p>Home trends are gravitating towards understated modernism. We’re seeing cleaner lines with more of a contemporary feel and not as textured as mid-century. </p>

                        <p>As someone who works with builders and developers, when I work with a client I focus more on figuring out what appeals to them. I don’t follow design trends per se, I prefer to look at what the house offers and what the client is looking for and I marry these two facets. This way, I know the client will be getting a final product that they’ll enjoy for years to come, regardless of what’s on trend. </p>

                        <p>For builders and developers, it’s important to listen to the client and make sure the house flows from the exterior to the interior, and finding those factors that make your client feel at home.</p>

                        <p>Now there are some really cool interiors in Ottawa. We’ve come a long way in the past 10 years to embrace avant garde design. With so many accessible resources online, people are raising their expectations and standards in interior design and discovering new possibilities. Now tradespeople and builders are more open to meeting that demand.
                        </p>
                        
