                        <p>We love myth-busting. One of our favourite urban legends that we love to dispel is that a luxury or custom home renovation can be tedious and a major headache.</p>
                        
                        <p>When you work with skilled tradespeople and passionate staff, you already have the first ingredients to a successful and satisfying home renovation. Of course materials, design aesthetic, municipal regulations, and other factors play a role, but we’ll let you in on a few other tips that can prepare your family and your home for an effortless luxury home renovation. </p>
                        
                        <p>Before diving into a home renovation, make sure you take time for these quick steps: </p>
                        
                        <h3>Know and Plan Your Budget</h3>
                        <p>When you know how much you’re willing to spend from the beginning, you are eliminating potential disappointments down the road. From the very beginning when determining any structural changes, to decisions about finishes and paint choices, knowing your budget helps prioritize “nice to have” and “need to have” items. If you’re struggling with choices, your contractor will be able to provide you with great insight as you plan your renovation - we’d love to hear from you (link to contact page) if you’re thinking about renovating!</p>
                        
                        <h3>Factor in a contingency</h3>
                        <p>That being said, you may run into unexpected surprises throughout your renovation process. Especially if your home is a heritage build or had previous owners, there may be some behind the scenes work that you didn’t know was done behind your walls. Make sure you account for a cushion of about 10% of your budget for unexpected costs.</p>
                        
                        <h3>Tell Everyone</h3>
                        <p>Well, maybe not everyone, but you’d be surprised how much more sympathetic and patient neighbours will be if you give them a heads up that your place will be a construction zone for the next little while. This is especially important if you’re landscaping or anything that may encroach on their living space.</p>
                        
                        <p>Another key player to advise is your insurance company prior to starting renovations, otherwise your policy may voided. </p>
                        
                        <h3>Choose a Designer that Suits Your Aesthetic</h3>
                        <p>Your contractor will be able to recommend some great designers - so never be afraid to reach out to the contractor you trust to put you in touch with someone they have likely worked with before and have a great relationship with. The more your renovation project team gels, the smoother the renovation will go. That being said, make sure to check out your potential designer’s portfolio and references to ensure they’ve got experience with the type of style you like and want. </p>
                        
