                        <p>As with most great masterpieces, we believe in the power of contrast and tension. And while we’re not claiming to be the next Picasso of luxury window and door suppliers in Ottawa, we are going to be really proud of our latest project in Rockcliffe.</p>

                        <h3>But what about all that talk around contrast and tension, you ask?</h3>

                        <p>Sometimes, in order to create a truly outstanding final product, you have to blend the old with the new, the modern with the traditional - and that’s exactly what we did at 320 Hillcrest, a completely refurbished home in the heart of one of Ottawa’s most iconic neighbourhoods.</p>
                        <figure>
                            <img class="img-responsive shadow" src="./img/20190524-blog/kitchen_windows.jpg" alt="Photo of a modern light-coloured kitchen with a wooden floor with a beautiful center isle">
                        </figure>

                        <p>Previously an embassy dwelling belonging to the Government of France, our friends at Gemstone were tasked with refurbishing the entire estate. It was completely stripped down to the barest of bones, and Solii was chosen to supply each custom window, and all interior and exterior doors.</p>

                        <p>As a luxury heritage home in downtown Ottawa, it was essential that we preserve the original aesthetic while offering modern luxuries like high insulation factors, easy maintenance, solid wood craftsmanship and custom finishes to perfectly match this Rockcliffe luxury home’s interior. To pay homage to home’s historic roots, we used Simulated Divided Lite muntins for a detailed finish.</p>

                        <p>Beyond the details, window placement and size were completely revamped to take full strategic advantage of the corner lot view. Windows were expanded, lengthened and added in elongated bays and under loft ceilings to let natural light pour in. Each window was custom made to fit the new specs, and because of the fine craftsmanship and detail of our <a href="https://www.solii.ca/about" target="_blank">Belisle windows</a>, we were able to attain a balance of history with contemporary touches.</p>

                        <figure>
                            <img class="img-responsive shadow" src="./img/20190524-blog/sink_window.jpg" alt="Photo of a modern kitchen sink in front of beautiful classic windows">
                        </figure>

                        <p>So if windows are the eyes to a home’s soul (this is what we tell ourselves), then what’s the front door? A great first impression perhaps? With this house, our solid African Mahogany door with sleek sidelites was the perfect way to make a winning first impression. The richness of solid handcrafted wood was a nod to the historic craftsmanship of the past, while the equilateral clean lines announce that this home is future-ready.</p>

                        <figure>
                            <img class="img-responsive shadow" src="./img/20190524-blog/front_door_2.jpg" alt="Photo of a large modern wooden door">
                        </figure>

                        <h3>How’s that for subtle contrast and tension?</h3>
                        <p>Inside, it’s a bit more obvious. Each custom handcrafted door and window feature contrasting matte black hinges, hardware and doorknobs. We love these pops of dark against light. To maintain consistency with the window muntins, all interior doors featured rectangular paneling for a contemporary take on classic detailing.</p>
                        <figure>
                            <img class="img-responsive shadow" src="./img/20190524-blog/hardware.jpg" alt="Photo of a modern door lock on a beautiful wooden door">
