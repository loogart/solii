                        <p>
                            The front door is the starting point of the journey through your home—a focal point not to be overlooked. If you are building a custom home in Ottawa, or looking to replace the front door of your current home, this guide will help you navigate the many choices on the market so you can make the best decision for <strong>you</strong>.</p>


                        <h3>1. Your Lifestyle</h3>
                        <p>More than the jumping off point of your home, upgrading your front door, garage, or patio door offers the opportunity to better accommodate your lifestyle. Do you have a waterfront view you’d like to showcase? Are you in the country and need a screen feature? Are you interested in maximizing the efficiency of your home? In Ottawa, the temperature plays a big part in home construction. </p>
                        <img src="./img/blog-luxury-door-2.jpg">
                        <figcaption class="figure-caption"><a href="http://cortneybishop.com/">Photo by Cortney Bishop Design</a></figcaption>
                        <h3>2. Your Aesthetic</h3>
                        <p>Everybody’s personal style is unique, and your doors should be no exception. When you choose the right front door for your aesthetic, the result will enhance your home for years to come.</p>

                        <h4>Material </h4>
                        <p>Our flexible luxury door options yield an abundance of choice that will positively impact the efficiency and style of your home.</p>
