                        <h4>Door Features</h4>
                        <p>From the frame to the hinges, you can customize every element of your new front door, and we make sure the finest details are scrutinized for performance, efficiency, and style. Often, it’s the small details that matter—here are some to consider when choosing your new custom door for your home. </p>
                        <p><i>Finishes and Stains:</i> Glossy or matte, rich or light, the choice of finishes can make your door a dramatic focal point or seamless integration into your home’s facade. Whether you’ve chosen solid wood or steel, we have an array of textures and colours to fit your style.</p>
                        <p><i>Divided Lights:</i> If you’ve chosen to have a glass pane or sidelight as part of your door, these grilles let you customize the look even further.</p>
                        <p><i>Hardware:</i> The final touches are in the hinges, knobs, and hardware—whether brass or brushed chrome, choose hardware that will compliment your home for a look you’ll love.</p>

                        <img src="./img/blog-luxury-door-4.jpg">
                        <h3>3. Economic &amp; Energy Efficiency </h3>
                        <p><i>Glass:</i> Opting for a glass insert in your new luxury custom door is entirely up to your stylistic preferences. In addition to aesthetics, a glass insert in your door can affect the thermal efficiency of your door. Especially in extreme climates like Ottawa, energy efficiency is a key factor. </p>
                        <p>We offer factory-glazed, low-e, and argon-filled glass options for the highest performance. Read more about our exclusive Technoform glass here! (link to technoform glass blog post) </p>
                        <p><i>Installation:</i> Our professional installation minimizes risks of infiltration or potential water damage, and is often necessary for warranties. Make sure your new custom luxury door is installed by a professional team, or a contractor that you trust. </p>
                        <p><i>Material:</i> Different materials offer different insulating factors which will affect the efficiency of your door. We offer both solid wood - a great natural insulator - as well as insulated aluminium, which is durable and offers thermal insulation properties. Your climate and the location of your door are also important factors to consider.</p>

                        <img src="./img/blog-luxury-door-5.jpg">

                        <h3>4. Good Things to Know Before Contacting your Architect, Designer, Builder, or Custom Door Specialist</h3>
                        <ul>
                            <li>Know what type of home you have (i.e. farmhouse, ranch style, bungalow, two-storey)</li>
                            <li>Know the (approximate) age of your home</li>
                            <li>Know how many doors you need to replace or want in your home</li>
                            <li>Know where you would like each door </li>
                            <li>Know what type of security you would like your door to offer (might vary based on if it’s a front, patio, or garage door)</li>
                            <li>Have an idea of what material you would prefer (wood, aluminum, steel, etc.) </li>
                            <li>Have an idea of which style you would like to incorporate</li>
                            <li>Know your budget range</li>
                        </ul>

                        <h3>Good Questions to Ask</h3>
                        <ul>
                            <li>What is the turnaround time between ordering and installation?</li>
                            <li>How long will installation take?</li>
                            <li>What happens if there is an issue after product installation? Ask for a breakdown of the warranty. </li>
                        </ul>
