                        <p>Ahhh the kitchen. Whether or not you consider yourself a master chef, there is no doubt that the kitchen always ends up being the gathering place, the heart of any home. Impromptu kitchen parties, guests who always seem to congregate while you chop up hors d’oeuvres, morning weekday chaos - it all happens in here.</p>

                        <p>Your luxury home deserves a home base that is worthy of every family circumstance, and since we’ve seen many a custom luxury home, we’ve picked out our all-time favourite kitchen features so you can create a luxury kitchen that fills your every need. </p>


                        <h3>Built-in Coffee Bar</h3>
                        <p>Your 6AM morning self with thank you when you can head to a coffee nook dedicated to brightening your mornings. Make sure to include a plumbed in coffee maker, warming drawer, and clear cabinets to easily access your favourite mug. You’re welcome. </p>

                        <h3>Double Dishwasher Drawers</h3>
                        <p>There’s a reason why restaurants have dishwasher drawers instead of the traditional type. Easier loading, more efficient access to clean plates, and because there are two drawers, you can keep a convenient rotation - ensuring dirty dishes always have somewhere to go and never stay in the sink. </p>

                        <h3>Workstation</h3>
                        <p>Recipes need checking, family schedules need to be handy - a small workstation with plenty of outlets for devices, phone chargers, and productivity paraphernalia will keep you clan happy. Pro tip: make sure this is located out of the way of the main cooking area to avoid traffic. </p>

                        <h3>Integrated Drawer Stool</h3>
                        <p>Got little ones that love to help out in the kitchen? Or maybe there’s a set of high cabinets that you’re tired of reaching for? Install a roll-out step stool in the kick plate for easy access and family fun time. </p>

                        <h3>Drawer Fridge for Snacks</h3>
                        <p>Most kitchens have an adjacent or open family room nearby. Keep a drawer fridge that is always stocked with juice boxes and healthy snacks to make after school hours and play dates a breeze. Pro tip: the best location for this is out of the way of the main cooking area and close to the family room so kids can help themselves too.</p>

                        <h3>Double Ovens</h3>
                        <p>Double ovens are not only an impressive-looking feature to any luxury kitchen, they’re also super efficient. Keep one for warming while the main dish gets cooking. This is a must for master entertainers. </p>

                        <h3>Divided Drawer Storage and Garbage</h3>
                        <p>It’s a lot easier to find appliances and tools when they are well organized. Banish those messy “everything drawers” with divided drawer storage and discover how fun it is to cook with everything readily at your fingertips. </p>
                        <p>Same goes with garbage - make sure to integrate paper, plastic, and wet waste to reduce messy triage work later. </p>
