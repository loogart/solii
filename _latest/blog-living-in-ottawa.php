                        <p>Ottawa.  </p>
                        <p>omehow, despite the above commonly used descriptors, Ottawa always finds itself at the top of “best of” lists for quality of life. We may be biased, but the internet can’t be. If you’re looking to move to this gorgeous, cultural-rich, historically-relevant city with enough quirk and bustle to attract millions every year, you’ve made the right choice. </p>
                        <p>Here’s everything you need to know about living in Canada’s capital city:</p>
                        
                        <h3>The Weather Reigns Supreme</h3>
                        
                        <p>In conversations, in daily life, in EVERYTHING. Why? Ottawa’s climate ranges from -40 degrees celcius in winter to +33 degrees degrees in the summer. At those extremes, mother nature is central to Ottawa’s activities - Winterlude, the Tulip Festival, Dragon Boat races, skating on the Rideau Canal are all part of how Ottawa residents embrace the weather.</p>
                        
                        <h3>Ottawa is a Healthy City</h3>
                        <p>With over 200 kilometers of bike paths, and the Gatineau Hills only minutes away from the urban core, it’s easy to stay active in Ottawa. During the winter, bike paths turn into cross country ski and snowshoe networks. And where else can you practice free yoga on the lawn of the seat of government? </p>
                        
                        <h3>Culture is Our Jam</h3>
                        
                        <p>Being the nation’s capital, Ottawa is a hub of culture and diversity. With embassies, consulates, and high commissions from over 130 countries, a cool diplomatic and political crowd is infused into the city. National Museums and epic events like Bluesfest and Ottawa Race Weekend bring people from all over the world to visit. </p>
                        
                        <h3>Technology is Our Jam, Too</h3>
                        <p>Shopify, QNX - these heavy hitters attract some of the best minds in the business, making Ottawa one of the most respected tech hubs in the country.</p>
                        
                        <h3>Ottawa Has Cool Neighbourhoods</h3>
                        <p>Much like large cosmopolitan cities, Ottawa boasts several cool boroughs that are worth exploring, and each has its own unique personality. From hipster-inspired Hintonburg to social-central neighbourhoods like the Glebe and the Byward Market, Ottawa offers locales for all tastes. </p>
                       
                       <h3>So Safe and So Clean</h3>
                       <p>Ottawa is <a href="http://www.huffingtonpost.ca/ypnexthome/crime-rates-canadian-cities_b_11185172.html" target="_blank">ranked the fourth safest city in the country</a> and is amongst the <a href="https://www.care2.com/causes/10-cities-with-the-cleanest-air-in-the-world.html" target="_blank">top 10 cities with the cleanest air in the world</a>. The perks of being surrounded by natural waterways and a giant National Park. </p>
                       
                       <h3>Ottawa is a Smart City</h3>
                       <p>Not like a smartphone, but the old-school kind of smart. Ottawa boasts top notch educational institutions: the University of Ottawa, Carleton University and Algonquin College.</p>
                       
