                        <p>If you are a luxury or custom home owner, you’ve likely heard the term “architectural window” before. But besides sounding like an exclusive descriptor in the pages of Architectural Digest or Dwell Magazine, what is an architectural window? How does it differ from a standard window and why is it often used in high end or luxury homes?</p>


                        <p>Despite its specific name, an architectural window does not represent one style of window, but the option to create a fully customized window with a wide range of options from shape, material, style, and finishes. This flexibility in design and function allows architectural windows to perfectly complement the architecture and style of any type of building.</p>

                        <p>Architectural windows are typically designed for the most discerning homeowners - those who have a strong appreciation for design, quality, and building integrity.</p>

                        <p>The benefits of architectural windows go far beyond their aesthetic beauty, and are top considerations for architects and homeowners alike.</p>

                        <h3>Design Flexibility and Freedom</h3>

                        <figure>
                            <img class="img-responsive shadow" src="./img/201904-blog/fenetre-oscillo-battant-design-06-big.jpg" alt="Photo of a modern home with big windows, wood and slate finishes">
                        </figure>

                        <p>Architectural windows are where Pinterest dreams come to life. Since architectural windows are free of the limitations of prefabricated windows, there are no constraints around size, shape, colour or finish.</p>

                        <p>Architectural windows offer the unique benefit of a truly personalized finished product - from interior to exterior windows, every aspect of your home can be especially tailored to your taste and design preferences. Specifically; tight, large, or seemingly awkward spaces can be enhanced and made functional by adding natural light via an architectural window.</p>

                        <p>Finally, freedom of design extends to the finest details like matching a highly specific colour or hardware style - your architectural window can be finished with any shade or metallic finish you like.</p>

                        <h3>Energy Efficiency and Your Home</h3>

                        <figure>
                            <img class="img-responsive shadow" src="./img/201904-blog/realisation-architecturale-fenetres-nocra-montreal-image-4-big.jpg" alt="Photo of a classic window">
                        </figure>

                        <p>High calibre architectural windows like the ones we offer possess of the moment technology for high energy efficiency like double insulated Low-E and Argon-filled glass. Modern factors like this not only increase the energy efficiency of your home, but they can also contribute to increasing your home’s overall value.</p>
                        <p>Architectural windows allow for modern benefits without compromising a building’s style.</p>

                        <h3>Character Defining Elements and Historic Preservation</h3>

                        <figure>
                            <img class="img-responsive shadow" src="./img/201904-blog/portes-et-fenetres-patrimoine-nocra-big.jpg" alt="Photo the entrance of a 19th century style building with very classic windows">
                        </figure>
                        <p>Architectural windows are often used in historic districts and heritage buildings for their prominence in maintaining the character defining elements of historic and architecturally iconic buildings.</p>

                        <p>Know that if you are restoring a heritage property in Ottawa (and beyond!), an architectural window will allow you to meet the building requirements of a heritage building. Through custom design, you can perfectly match the colour, material, and stylistic elements of a heritage building while improving energy efficiency and maintaining all the functionality of a modern building.</p>

                        <p>The same can be said for contemporary buildings and luxury homes; use sleek aluminum and low-profile windows to complement a minimalistic facade, or choose a solid wood option to draw out the signature traditional style of a Tudor or Victorian home.</p>

