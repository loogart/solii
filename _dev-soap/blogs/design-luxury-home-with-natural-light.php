                           <h5>On Energy Consumption: </h5>
                            <p class="mb-1">“Spaces outfitted with daylight-sensing controls can reduce the energy used for electric lighting by 20 percent to 60 percent, according to the studies “Photoelectric Control: The Effectiveness of Techniques to Reduce Switching Frequency” (2001) and “Summertime Performance of an Automated Lighting and Blinds Control System””</p>
                            <footer class="blockquote-footer"><a href="https://www.archlighting.com/technology/the-benefits-of-natural-light_o" target="_blank">Source</a></footer>
