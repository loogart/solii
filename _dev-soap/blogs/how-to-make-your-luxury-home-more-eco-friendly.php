<h2>What to look for :</h2>

						
						<h3>Low-E Glass</h3>
						<p>This is a coating applied to the glass of your window which pushes the heat back in the direction it came from. In the winter months, heat is retained, and in the summer, it is pushed back towards the sun, keeping your home cool. Both of these instances reduce the home’s dependence on resource-intense functions like heating and cooling.</p>

						<h3>Argon Filled Glass</h3>
						<p>This odorless, non-toxic gas sits between two glass window panes and prevents heat loss, increases the window’s R-factor (its insulating properties), and prevents frost and condensation on your windows. Similar to Low-E glass, it allows your heating and cooling to function more efficiently.</p>



						<h3>Aluminum Frames</h3>
						<p>Not only is aluminum one of the most easy to recycle materials, but it offers great longevity and durability.</p>


						<h3>Proper Sealing and Installation</h3>
						<p>Proper installation is key to ensuring your windows are as efficient and eco-friendly as possible. Weather stripping and caulking prevent gaps and air leaks.</p>
