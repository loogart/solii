                        <p>There’s nothing worse than looking out your window and having a gorgeous view eclipsed by spots, streaks, dust, and fingerprints.</p>

                        <p>So in the absence of this elusive technology, here are some need to know pro window cleaning tips.</p>

                        <h3>1. Consistency is Key </h3>
                        <p>Like most things in life, cleaning your windows on a regular basis is the key to never waking up to a murky view and thinking “when was the last time I cleaned my windows? (or had my windows cleaned?)” Set a schedule that works for you—whether it’s every three months or bi-annually, put it in your phone, and stick to it. </p>

                        <h3>2. Get the Right Tools </h3>
                        <p>The pros know what’s up. A professional grade squeegee is one of the best tools for leaving your windows looking crystal clear. Make sure you think about the size and type of window before purchasing one. Multi-pane windows will need a narrow squeegee to fit between the muntins, while picture windows need as long a squeegee as you can find. </p>
                        <p>A dash of liquid soap in warm water is the only solution you need—avoid anything too soapy or sudsy. </p>
                        <p>Finally, a used linen napkin or old cloth diapers are perfect for wiping your squeegee after each stroke. </p>

                        <h3>3. It’s All in the Wrist—Technique </h3>
                        <p>Just kidding—the most important part about ensuring a clean finish is by staying consistent in your direction—whether it’s horizontal or vertical, stick with it to avoid too many streaks. If you have a large picture window, start at the top left (if you’re right handed, left handers start at the top right) and work your way down in an S shape. Don’t forget to wipe your squeegee every time you swipe. </p>
                        <p>Finally, wipe the edges with a chamois, which leaves no streaks. </p>

