                        <h3>Le Chateau Frontenac</h3>

                        <img class="img-responsive" src="./img/blog-img/2019-02-24/1_interior.jpg" alt="Photo of the interior of the Chateau Frontenac">

                        <p>Apparently, Le Chateau Frontenac is said to be <a href="https://www.quebecregion.com/en/quebec-city-and-area/chateau-frontenac/" target="_blank">the world’s most photographed hotel</a>. Any guesses why? Perhaps because this hotel was the prototype for the now iconic string of chateau-style railway hotels built by the Canadian Pacific Railway, and is the purest expression of Chateau design among them all. The Chateau Frontenac was designed by architect Bruce Price who was particularly inspired by the medieval chateaux of the Loire Valley in France. Telling signs of this influence can be found throughout this historic landmark. The massive scale and fortress-like silhouette, circular and polygonal towers, and the steeply pitched roof define the building’s heritage character. </p>

                        <p>Our favourite details though? The window dressing. Ornate gables and dormers frame windows at various levels, and above fourth storey windows, a row of machicolations are found in true Gothic style. Solid wood arched windows and stained glass transoms finish the stunning effect. </p>

                        <p>Historic significance, antique structural details and a national landmark -Belisle’s custom architectural solid wood windows are a perfect pairing to pay homage to the Chateau Frontenac’s history while offering comforts of the 21st century. </p>

